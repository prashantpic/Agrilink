import React, { useState, useEffect } from 'react';
import { View, Text, TextInput, Button, StyleSheet, ScrollView, Alert } from 'react-native';
import { StackNavigationProp } from '@react-navigation/stack';
import { RouteProp } from '@react-navigation/native';
import { database } from '../../../../core/data/local/db';
import Farmer from '../../../../core/data/local/models/Farmer';
import SyncQueueItem from '../../../../core/data/local/models/SyncQueueItem';
import { OfflineIdGenerator } from '../../../../core/sync/OfflineIdGenerator';
import { SYNC_STATUS_PENDING_CREATE, SYNC_STATUS_PENDING_UPDATE, OPERATION_TYPE_CREATE, OPERATION_TYPE_UPDATE } from '../../../../core/constants/syncConstants'; // Assuming these constants exist

// Define a placeholder for your RootStackParamList if not already defined globally
type RootStackParamList = {
  FarmerFormScreen: { farmerId?: string };
  // ... other screens
};

type FarmerFormScreenNavigationProp = StackNavigationProp<RootStackParamList, 'FarmerFormScreen'>;
type FarmerFormScreenRouteProp = RouteProp<RootStackParamList, 'FarmerFormScreen'>;

interface Props {
  navigation: FarmerFormScreenNavigationProp;
  route: FarmerFormScreenRouteProp;
}

const FarmerFormScreen: React.FC<Props> = ({ navigation, route }) => {
  const farmerId = route.params?.farmerId;
  const [isEditing, setIsEditing] = useState(!!farmerId);

  const [firstName, setFirstName] = useState('');
  const [lastName, setLastName] = useState('');
  const [primaryPhone, setPrimaryPhone] = useState('');
  const [dateOfBirth, setDateOfBirth] = useState(''); // Store as ISO string e.g. YYYY-MM-DD

  // TODO: Add other Farmer fields as per src/core/data/local/models/Farmer.ts
  // e.g., middleName, genderId, secondaryPhone, email, nationalIdNumber, nationalIdType, familySize, farmingExperienceYears, preferredLanguageId, statusId

  useEffect(() => {
    if (isEditing && farmerId) {
      const loadFarmerData = async () => {
        try {
          const farmerToEdit = await database.get<Farmer>('farmers').find(farmerId);
          setFirstName(farmerToEdit.firstName);
          setLastName(farmerToEdit.lastName);
          setPrimaryPhone(farmerToEdit.primaryPhone);
          setDateOfBirth(new Date(farmerToEdit.dateOfBirth).toISOString().split('T')[0]); // Assuming dateOfBirth is stored as number/timestamp
          // TODO: Load other fields
        } catch (error) {
          console.error('Failed to load farmer data for editing:', error);
          Alert.alert('Error', 'Could not load farmer data.');
        }
      };
      loadFarmerData();
    }
  }, [isEditing, farmerId]);

  const handleSubmit = async () => {
    // Basic validation
    if (!firstName.trim() || !lastName.trim() || !primaryPhone.trim() || !dateOfBirth.trim()) {
      Alert.alert('Validation Error', 'Please fill in all required fields.');
      return;
    }

    const farmerData = {
      firstName: firstName.trim(),
      lastName: lastName.trim(),
      primaryPhone: primaryPhone.trim(),
      dateOfBirth: new Date(dateOfBirth).getTime(), // Store as timestamp
      // TODO: Add other fields from state
    };

    try {
      await database.write(async writer => {
        let farmerRecord: Farmer;
        let operationType: string;

        if (isEditing && farmerId) {
          const existingFarmer = await writer.collections.get<Farmer>('farmers').find(farmerId);
          farmerRecord = await existingFarmer.update(farmer => {
            farmer.firstName = farmerData.firstName;
            farmer.lastName = farmerData.lastName;
            farmer.primaryPhone = farmerData.primaryPhone;
            farmer.dateOfBirth = farmerData.dateOfBirth;
            // TODO: Update other fields
            farmer.sync_status = SYNC_STATUS_PENDING_UPDATE;
            farmer.last_modified_locally_at = Date.now();
            farmer.updated_at = Date.now();
          });
          operationType = OPERATION_TYPE_UPDATE;
        } else {
          const localId = OfflineIdGenerator.generate();
          farmerRecord = await writer.collections.get<Farmer>('farmers').create(farmer => {
            farmer._raw.id = localId; // WatermelonDB specific way to set ID if it's not auto-generated by DB
            farmer.firstName = farmerData.firstName;
            farmer.lastName = farmerData.lastName;
            farmer.primaryPhone = farmerData.primaryPhone;
            farmer.dateOfBirth = farmerData.dateOfBirth;
            // TODO: Set other fields
            // farmer.statusId = SOME_DEFAULT_STATUS; // Example
            farmer.sync_status = SYNC_STATUS_PENDING_CREATE;
            farmer.last_modified_locally_at = Date.now();
            farmer.created_at = Date.now();
            farmer.updated_at = Date.now();
          });
          operationType = OPERATION_TYPE_CREATE;
        }

        // REQ-14-001: Create SyncQueueItem
        await writer.collections.get<SyncQueueItem>('sync_queue_items').create(item => {
          item.entity_id = farmerRecord.id;
          item.entity_type = 'Farmer'; // Match model table name or a defined constant
          item.operation_type = operationType;
          item.payload = JSON.stringify(farmerData); // Optional: store payload for easier retry/debug
          item.attempt_count = 0;
          item.created_at = Date.now();
        });
      });

      Alert.alert('Success', `Farmer data ${isEditing ? 'updated' : 'saved'} locally.`);
      navigation.goBack();
    } catch (error) {
      console.error('Failed to save farmer data:', error);
      Alert.alert('Error', `Could not save farmer data. ${error}`);
    }
  };

  return (
    <ScrollView style={styles.container}>
      <Text style={styles.label}>First Name:</Text>
      <TextInput style={styles.input} value={firstName} onChangeText={setFirstName} placeholder="Enter first name" />

      <Text style={styles.label}>Last Name:</Text>
      <TextInput style={styles.input} value={lastName} onChangeText={setLastName} placeholder="Enter last name" />

      <Text style={styles.label}>Primary Phone:</Text>
      <TextInput style={styles.input} value={primaryPhone} onChangeText={setPrimaryPhone} placeholder="Enter primary phone" keyboardType="phone-pad" />

      <Text style={styles.label}>Date of Birth (YYYY-MM-DD):</Text>
      <TextInput style={styles.input} value={dateOfBirth} onChangeText={setDateOfBirth} placeholder="YYYY-MM-DD" />

      {/* TODO: Add TextInput components for other Farmer fields */}

      <Button title={isEditing ? 'Update Farmer' : 'Save Farmer'} onPress={handleSubmit} />
    </ScrollView>
  );
};

const styles = StyleSheet.create({
  container: {
    flex: 1,
    padding: 16,
  },
  label: {
    fontSize: 16,
    marginBottom: 4,
    color: '#333',
  },
  input: {
    borderWidth: 1,
    borderColor: '#ccc',
    padding: 10,
    marginBottom: 16,
    borderRadius: 4,
    backgroundColor: '#fff',
  },
});

export default FarmerFormScreen;
